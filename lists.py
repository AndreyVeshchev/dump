# Дано нечетное число n
n = 9  # Пример нечетного числа, можно изменить на любое другое нечетное число

# Создаем двумерный массив n x n, заполненный символами "."
array = [["." for i in range(n)] for j in range(n)]

# Находим индекс средней строки и среднего столбца
middle_index = n // 2

# Заполняем среднюю строку символами "*"
for j in range(n):
    array[middle_index][j] = "*"

# Заполняем средний столбец символами "*"
for i in range(n):
    array[i][middle_index] = "*"

# Заполняем главную диагональ символами "*"
for i in range(n):
    array[i][i] = "*"

# Заполняем побочную диагональ символами "*"
for i in range(n):
    array[i][n - 1 - i] = "*"

# Выводим полученный массив на экран, разделяя элементы пробелами
for row in array:
    print(" ".join(row))

