import random  # Импортируем модуль random для генерации случайных чисел

# Задаем количество четных чисел, которые нужно сгенерировать
n = 10  # Пример: можно изменить на любое другое количество четных чисел

# Инициализируем пустой список для хранения четных чисел
even_numbers = []

# Генерируем массив из n четных чисел в диапазоне [20, 70]
for i in range(n):  # Цикл для генерации n чисел
    even_number = random.choice(range(20, 71))  # Выбираем случайное четное число
    even_numbers.append(even_number)  # Добавляем число в список

# Записываем числа из массива в текстовый файл
with open('even_numbers.txt', 'w') as file:  # Открываем файл для записи
    for number in even_numbers:  # Проходим по каждому числу в массиве
        file.write(str(number) + '\n')  # Записываем число в файл, добавляя перевод строки

# Выводим на экран k-й элемент массива
k = 3  # Пример: индекс элемента, который нужно вывести (индексация начинается с 0)
if 0 <= k < n:  # Проверяем, что индекс k находится в пределах массива
    print("k-й элемент массива:", even_numbers[k])  # Выводим k-й элемент массива
else:
    print("Индекс k вне диапазона массива")  # Сообщаем, если индекс вне диапазона
