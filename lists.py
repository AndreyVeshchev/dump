# ЗАДАНИЕ 1
# Создаем пример одномерного массива
array = [1, -2, 3, -4, 5, -6, 7]

# Создаём переменные для хранения сумм
positive_sum = 0  # Сумма положительных элементов
negative_sum = 0  # Сумма отрицательных элементов

# Проходим по каждому элементу массива
for i in array:
    # Проверяем, является ли элемент положительным
    if i > 0:
        positive_sum += i  # Добавляем к положительной сумме
    # Проверяем, является ли элемент отрицательным
    elif i < 0:
        negative_sum += i  # Добавляем к отрицательной сумме

# Выводим результаты
print("Сумма положительных элементов:", positive_sum)
print("Сумма отрицательных элементов:", negative_sum)

